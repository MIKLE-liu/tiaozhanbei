function trend(seed: number, base: number) {
  const arr: { month: string; value: number }[] = []
  for (let i = 0; i < 12; i++) {
    const x = Math.sin((i + seed) * 0.7) * 8 + Math.cos((i + seed) * 1.3) * 4
    arr.push({ month: `${i + 1}月`, value: Math.max(10, Math.round(base + x)) })
  }
  return arr
}

